# MULTIPLATFORM TYPE ALIASES

type
  byte* = uint8 # Java/C#-like "uint8 type"-like alias
  sbyte* = int8 # C#-like "int8 type"-like signed byte alias
  boolean* = bool # Java/Pascal-like "bool type"-like alias
  integer* = int # Pascal-like "int type"-like alias
  real* = float # Pascal-like "float type"-like alias
